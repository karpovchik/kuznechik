Exp_table = [0]*510

Log_table = [-1]*256

def GF256_Generate_Table():
    P = 0x1C3           # irReducible polynomial x^8 + x^7 + x^6 + x + 1

    x = 1
    for i in range(255):
        Exp_table[i] = x
        Log_table[x] = i
        
        x <<= 1          # mult x by 2
        # checking for overflow
        if x & 0x100:
            x ^= P   # reduce overflowed value by polynomial

    # repeat a table to make further requests without (mod 255)
    for i in range(255, 510):
        Exp_table[i] = Exp_table[i - 255]     # 255 is a number of nonzero elements

def GF256_MUL(a : int, b : int) -> int:
    if (a == 0 or b == 0):
        return 0
    else:
        return Exp_table[Log_table[a] + Log_table[b]]
    
def X(block1 : list, block2 : list) -> list:
    res = []
    for i in range(16):
        res.append(hex(int(block1[i], 16) ^ int(block2[i], 16)))

    return res

def S(block : list) -> list:
    res = []
    for byte in block:
        byte = byte[2:]
        if (len(byte) == 1) : byte = f"0{byte}"
        big_nibble = int(byte[0], 16)           
        lil_nibble = int(byte[1], 16)

        new_byte = Pi[big_nibble][lil_nibble]    # get a new byte from 'Pi' matrix
        res.append(hex(new_byte))
    
    return res

def S_reverse(block : list) -> list:
    res = []
    for byte in block:
        byte = byte[2:]
        if (len(byte) == 1) : byte = f"0{byte}"
        big_nibble = int(byte[0], 16)           
        lil_nibble = int(byte[1], 16)

        new_byte = Pi_reverse[big_nibble][lil_nibble]    # get a new byte from 'Pi' matrix
        res.append(hex(new_byte))

    return res

def L(block : list) -> list:
    res = block

    for i in range(16):
        acc = 0
        for j in range(16):
            byte = res[j]
            gf_mul = GF256_MUL(int(byte[2:], 16), L_vector[j])      # iterative multiplication 
            acc ^= gf_mul                                           # accumulator - new byte
        # R-shift
        res.insert(0, hex(acc))
        res.pop()
    
    return res

def L_reverse(block : list) -> list:
    res = block

    for i in range(16):
        acc = 0
        for j in range(15):
            byte = res[j + 1]
            gf_mul = GF256_MUL(int(byte[2:], 16), L_vector[j])      # iterative multiplication_
            acc ^= gf_mul                                           # accumulator - new byte_
        acc = int(res[0][2:], 16)^acc                               # calculate previous last value
        for i in range(L_vector[-1] - 1):
            acc -= L_vector[-1]

        # L-shift
        res.append(hex(acc))
        res.pop(0)
    
    return res

def F(rnd_key : list, c : list) -> list:
    res = L(S(X(rnd_key, c)))

    return res

def K(key : list) -> list:
    RND_KEYS = []
    Lkey = key[0 : len(key) // 2]               # round key 1
    Rkey = key[len(key) // 2 : len(key)]        # round key 2
    k = 0

    RND_KEYS.append(Lkey)
    RND_KEYS.append(Rkey)

    for i in range(4):
        for j in range(8):
            Ck = [ hex(0) for i in range(15) ]
            Ck.append(hex(k + 1))              # form the last byte of iterative constant
            L_C = L(Ck)                        # calculate L(Cij)

            f = F(Lkey, L_C)                    # L(S(X({}, {}))) convertion
            x = X(f, Rkey)                      # just XOR

            tmp_key = Lkey
            Lkey = x
            Rkey = tmp_key

            k += 1

        RND_KEYS.append(Lkey)
        RND_KEYS.append(Rkey)
    
    return RND_KEYS

def E(b : list, k : list) -> list:
    block = b

    for i in range(9):
        block = L(S(X(block, k[i])))

    return X(block, k[-1])

def D(b : list, k : list) -> list:
    block = b

    for i in range(9, 0, -1):
        block = S_reverse(L_reverse(X(block, k[i])))

    return X(block, k[0])